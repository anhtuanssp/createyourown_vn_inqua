	<script type="text/javascript">
		(function($,window,document,undefined){
			'use strick';
			var huongdanModule = function(){
				this.$elModule = $('.cicle-block');
				this.init = function(){
					var that = this;
					this.$elModule.find('> h3')
						.click(function(event) {

							$(this).toggleClass('isTour');
							that.$elModule.find('.description').hide();
							// that.$elModule.find('> h3').find('i').removeClass().addClass('ion-plus-round')
							that.$elModule.find('> h3').not($(this)).removeClass('isTour')
							that.$elModule.css({
								width: '45%',
							});

							var des = $(this).next('.description');
							var itsModule = $(this).parent();

							itsModule.css({
								width: '95%',
							});
							if($(this).hasClass('isTour')){
								itsModule.css({
									width: '95%',
								});
								// $(this).find('i').removeClass().addClass('ion-minus-round')
								des.show();
							}else{
								itsModule.css({
									width: '45%',
								});
								// $(this).find('i').removeClass().addClass('ion-plus-round')
								des.hide();
							}
						});
				
					$('.video-trigger').click(function(event) {
						/* Act on the event */
						var videoNext = $(this).next();
						videoNext.toggle('fast');
					});

					$('.close-video').click(function(event) {
						/* Act on the event */
						$(this).parent().prev().trigger('click')
					});


				};
				return this;
			};
			(new huongdanModule()).init();
		})(jQuery,window,document);
	</script>


